    private void jBCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCadastrarActionPerformed
        try {
            dao = new DAO<>(Valores.class);
            valor = new Valores();

            valor.setEstado(jTEstado.getText());
            valor.setPorcentagem(Double.parseDouble(jTPorcentagem.getText()));

            dao.adicionar(valor);
            JOptionPane.showMessageDialog(this, "Valor adicionado com sucesso!", "Activity Performed Successfully", JOptionPane.INFORMATION_MESSAGE);
            atualiza();
        } catch (NumberFormatException | DataException ex) {
            JOptionPane.showMessageDialog(null, ex, "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBCadastrarActionPerformed

