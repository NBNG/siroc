    private void atualiza() {
        ControleValores cv = new ControleValores(painel);
        painel.add(cv);
        this.dispose();
        cv.setVisible(true);
    }

    private void populateFields() {
        dao = new DAO<>(Valores.class);
        lista = dao.listaTodos();

        while (tmValor.getRowCount() > 0) {
            tmValor.removeRow(0);
        }

        for (int i = 0; i < lista.size(); i++) {
            tmValor.addRow(new String[]{null, null, null, null});
            tmValor.setValueAt(lista.get(i).getEstado(), i, 0);
            tmValor.setValueAt(lista.get(i).getPorcentagem() + "%", i, 1);

        }

    }
